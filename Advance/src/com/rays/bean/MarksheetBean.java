package com.rays.bean;

public class MarksheetBean {
	
	
	private int Rollno;
	private String Name ;
	private int Marks;
	
	
	public int getRollno() {
		return Rollno;
	}
	public void setRollno(int Rollno) {
		this.Rollno = Rollno;
	}
	public String getName() {
		return Name;
	}
	public void setName(String Name) {
		this.Name = Name;
	}
	public int getMarks() {
		return Marks;
	}
	public void setMarks(int marks) {
		Marks = marks;
	}
	
	

}



